                    <h4 id="tentang">
                        TENTANG KAMI </h4>
                    <div class="col">
                        <p>Perkembangan sektor konstruksi sekarang ini, masih dijadikan tolak ukur dari kemajuan suatu
                            negara. Dalam pembangunan Nasional, sektor konstruksi mempunyai peranan penting sebagai
                            barometer pertumbuhan ekonomi. Untuk itu, Totalindo Eka Persada TBK ("Totalindo" /
                            "Perseroan") yang bergerak di bidang jasa konstruksi swasta dengan spesialisasi pada gedung
                            bertingkat, ingin menjadi bagian dalam pembangunan Nasional.</p>
                        <p>Pendiri mengawali karirnya pada tahun 1995 dengan konstruksi pertama, Mall dan Condominium
                            Taman Anggrek yang merupakan superblok terbesar di kawasan Asia Tenggara pada saat itu,
                            Totalindo terus mengembangkan bisnisnya dan secara resmi berdiri pada tahun 1996. Perseroan
                            memiliki pengalaman lebih dari 20 tahun senantiasa memberikan layanan dan kualitas yang
                            terbaik hingga saat ini dan telah dipercaya untuk membangun Mall, Apartemen, Hotel dan
                            berbagai gedung perkantoran di Indonesia. Keahlian Totalindo tidak terbatas dalam
                            pembangunan gedung bertingkat, tetapi juga dalam pembangunan infrastruktur seperti jalan
                            tol, jalan layang dan jembatan.</p>
                        <p>Dengan berpegang teguh pada Komitmen, Integritas dan Dedikasi sebagai nilai-nilai dasar
                            Perseroan, Totalindo bertujuan untuk terus berkontribusi terhadap perkembangan industri di
                            Indonesia.</p>
                        <p>Totalindo percaya, bahwa inovasi yang berkelanjutan serta penerapan prinsip Tata Kelola
                            Perusahaan yang Baik (GCG) akan sangat berperan penting dalam meningkatkan profesionalisme
                            dan kinerja Perseroan di masa depan. Oleh sebab itu, Totalindo mewujudkan rencana menjadi
                            perusahaan terbuka dan mencatatkan saham perdana (IPO) di Bursa Efek Indonesia pada tanggal
                            16 Juni 2017 dengan kode saham &ldquo;TOPS&rdquo;. Sebanyak 1.666.000.000 saham baru yang
                            mewakili sebesar 24,99% kepemilikan masyarakat, dari modal ditempatkan dan disetor penuh.
                            Dengan demikian, saham yang dicatatkan pada Bursa Efek Indonesia sebanyak 6.666.000.000
                            lembar saham (100%) dari modal ditempatkan dan disetor penuh dalam Perseroan.</p>
